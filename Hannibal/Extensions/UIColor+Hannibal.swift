import UIKit

extension UIColor {
    static func primaryOrangeColor() -> UIColor {
        return UIColor(red:0.969,  green:0.580,  blue:0.118, alpha:1)
    }

    static func secondaryBlueColor() -> UIColor {
        return UIColor(red:0.191,  green:0.525,  blue:0.998, alpha:1)
    }

    static func tertiaryPurpleColor() -> UIColor {
        return UIColor(red:0.890,  green:0.407,  blue:0.839, alpha:1)
    }

    static func darkPurpleColor() -> UIColor {
        return UIColor(red:0.006,  green:0.028,  blue:0.200, alpha:1)
    }
}
